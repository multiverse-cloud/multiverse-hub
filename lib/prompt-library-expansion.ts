function buildSeoTitle(title: string, models: PromptModelId[]) {
  return `${title} Prompt - ${models.join(', ')}`
}

function createSeed(seed: PromptSeed): PromptSeedEntry {
  const categoryVisual = CATEGORY_VISUALS[seed.category]

  return {
    slug: seed.slug,
    title: seed.title,
    seoTitle: buildSeoTitle(seed.title, seed.models),
    metaDescription: seed.description || seed.summary,
    summary: seed.summary,
    description: seed.description || seed.summary,
    category: seed.category,
    subcategory: seed.subcategory,
    models: seed.models,
    tags: seed.tags,
    audience: seed.audience,
    visualStyle: seed.visualStyle || categoryVisual.visualStyle,
    previewImage: categoryVisual.previewImage,
    previewAlt: categoryVisual.previewAlt,
    featured: seed.featured,
    prompt: seed.prompt,
    variables: seed.variables || [],
    bestFor: seed.bestFor,
    workflow: seed.workflow,
    tips: seed.tips,
    examples: seed.examples,
  }
}

