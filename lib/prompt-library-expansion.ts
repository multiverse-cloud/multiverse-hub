type PromptSeed = {
  slug: string
  title: string
  summary: string
  description?: string
  category: PromptCategoryId
  subcategory: string
  models: PromptModelId[]
  tags: string[]
  audience: string
  featured?: boolean
  visualStyle?: string
  prompt: string
  variables?: PromptVariable[]
  bestFor: string[]
  workflow: string[]
  tips: string[]
  examples: PromptExample[]
}

type PromptSeedEntry = Omit<PromptEntry, 'id' | 'categoryTitle' | 'relatedSlugs' | 'updatedAt'>

